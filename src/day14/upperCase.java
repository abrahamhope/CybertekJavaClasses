package day14;

public class upperCase {
    public static void main(String[] args) {
        String city = "New York";
        System.out.println(city);

        System.out.println(city.toUpperCase() );
        System.out.println(city.toLowerCase() );

        System.out.println(city);

        String lan = "Java";
        System.out.println(lan);
        lan = lan.toUpperCase();

        System.out.println(lan);

        lan = lan.toLowerCase();
        System.out.println(lan);

    }
}
